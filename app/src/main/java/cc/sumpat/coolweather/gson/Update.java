package cc.sumpat.coolweather.gson;

public class Update {

    /**
     * loc : 2019-02-12 08:57
     * utc : 2019-02-12 00:57
     */

    private String loc;
    private String utc;

    public String getLoc() {
        return loc;
    }

    public void setLoc(String loc) {
        this.loc = loc;
    }

    public String getUtc() {
        return utc;
    }

    public void setUtc(String utc) {
        this.utc = utc;
    }
}
